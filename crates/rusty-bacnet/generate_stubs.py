def generate_stubs() -> str:
    """Generate the complete .pyi stub content by introspecting the module."""
    import rusty_bacnet as rb

    parts: list[str] = [HEADER]

    # Exceptions
    parts.append(EXCEPTION_STUBS)

    # Enums (with runtime-introspected variants)
    for name, raw_type in ENUM_CLASSES:
        cls = getattr(rb, name)
        parts.append(generate_enum_stub(cls, name, raw_type))

    # Composite types
    parts.append(OBJECT_IDENTIFIER_STUB)
    parts.append(PROPERTY_VALUE_STUB)
    parts.append(DISCOVERED_DEVICE_STUB)
    parts.append(COV_NOTIFICATION_STUB)
    parts.append(COV_NOTIFICATION_ITERATOR_STUB)

    # Main classes
    parts.append(BACNET_CLIENT_STUB)
    parts.append(BACNET_SERVER_STUB)
    parts.append(SC_HUB_STUB)

    return "\n".join(parts)


def main() -> None:
    stub_content = generate_stubs()

    # Write to the package directory alongside __init__.py
    stub_dir = Path(__file__).parent / "rusty_bacnet"
    stub_dir.mkdir(exist_ok=True)

    stub_path = stub_dir / "__init__.pyi"
    stub_path.write_text(stub_content)
    print(f"Wrote stubs to {stub_path}")

    # Ensure py.typed marker exists
    py_typed = stub_dir / "py.typed"
    if not py_typed.exists():
        py_typed.write_text("")
        print(f"Created {py_typed}")


if __name__ == "__main__":
    main()
