def generate_enum_stub(cls: type, name: str, raw_type: str) -> str:
    """Generate stub for a PyO3 enum-like class with class-level variants."""
    attrs = sorted(
        a for a in dir(cls)
        if not a.startswith("_") and a not in ("from_raw", "to_raw")
    )

    lines = [f"class {name}:"]
    # Class-level variant attributes
    for attr in attrs:
        lines.append(f"    {attr}: {name}")
    lines.append("")
    lines.append(f"    @staticmethod")
    lines.append(f"    def from_raw(value: {raw_type}) -> {name}: ...")
    lines.append(f"    def to_raw(self) -> {raw_type}: ...")
    lines.append(f"    def __repr__(self) -> str: ...")
    lines.append(f"    def __eq__(self, other: object) -> bool: ...")
    lines.append(f"    def __hash__(self) -> int: ...")
    lines.append("")
    return "\n".join(lines)


